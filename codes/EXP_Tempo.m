for i = 1:numT
    % Data read  
    eval(['RawData = X',num2str(i),';']);
%     Data{i}.rawdata = RawData;
    T = RawData(:,1);
    Glu = sum(RawData(:,2:3),2)/2;
    Xyl = sum(RawData(:,4:5),2)/2;
    Eth = sum(RawData(:,6:7),2)/2;
    OD = sum(RawData(:,8:9),2)/2;

    Data{i}.ydata = [T,Glu,Xyl,Eth,OD];

    % Validate that the experiment measured the time point when the second sugar is added
    if max(T(T<=abs(delay_time(i)))) ~= abs(delay_time(i))
        disp("Warning: the experiment didn't measure the time point when the second sugar is addded.");
    end    

    DelayCase = sign(delay_time(i));
    Data{i}.delaycase = DelayCase;
    Data{i}.delaytime = delay_time(i);
    Data{i}.system = 2; % temporal DOL

    % Initial condition setting for simulation
    % Xyl(1); Glu(2); Eth(3); Nx(4); Ng(5); Rx(6); Rg(7); Oxy(8); NAD(9); 
    Data{i}.y0 = NaN * zeros(9,1);
    switch DelayCase
        case -1
            Data{i}.y0(1) = Glu(1); 
            % initial glucose concentration equals the measurement at t = 0.
            Data{i}.y0(2) = sum(Xyl(T<=abs(delay_time(i))))/sum(T<=abs(delay_time(i)));
            % initial xylose concentration equals the average of measurements before YX is added.
            Data{i}.y0(3) = 0; % ethanol concentration
            Data{i}.y0(4) = 0; % YX strain
            Data{i}.y0(5) = OD(1); % YG strain
            Data{i}.y0(6:7) = 0; % intracellular precursor
            Data{i}.y0(8:9) = 1; % initial oxygen and cofactor concentration are normalized to 1
            Data{i}.secondstrain = OD(T==(abs(delay_time(i))+1)) - OD(T==abs(delay_time(i)));
            % The initial OD of the second strain is approximated based on
            % the OD difference before and after adding it
        case 0
            Data{i}.y0(1) = Glu(1); 
            % initial glucose concentration equals the measurement at t = 0.
            Data{i}.y0(2) = Xyl(1);
            % initial xylose concentration equals the measurement at t = 0.
            Data{i}.y0(3) = 0; % ethanol concentration
            Data{i}.y0(4) = OD(1)/2; % YX strain
            Data{i}.y0(5) = OD(1)/2; % YG strain; Assuming YX and YG are initially added in 1:1 ratio
            Data{i}.y0(6:7) = 0; % intracellular precursor
            Data{i}.y0(8:9) = 1; % initial oxygen and cofactor concentration are normalized to 1
            Data{i}.secondstrain = NaN;
        case 1
            Data{i}.y0(1) = sum(Glu(T<=abs(delay_time(i))))/sum(T<=abs(delay_time(i)));
            % initial glucose concentration equals the average of measurements before YG is added.
            Data{i}.y0(2) = Xyl(1); 
            % initial xylose concentration equals the measurement at t = 0.
            Data{i}.y0(3) = 0; % ethanol concentration
            Data{i}.y0(4) = OD(1); % YX strain
            Data{i}.y0(5) = 0; % YG strain
            Data{i}.y0(6:7) = 0; % intracellular precursor
            Data{i}.y0(8:9) = 1; % initial oxygen and cofactor concentration are normalized to 1
            Data{i}.secondstrain = OD(T==(abs(delay_time(i))+1)) - OD(T==abs(delay_time(i)));
            % The initial OD of the second strain is approximated based on
            % the OD difference before and after adding it
    end
